    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        try{
            ArrayList<Object> dados = new ArrayList<>();
            dados.add(this.campoNome.getText());
            dados.add(this.campoTelefone.getText());
            dados.add(this.campoEndereco.getText());
            if(this.opcaoCNPJ.isSelected()){
                dados.add(this.campoCNPJ.getText());
            }else{
                dados.add(this.campoCPF.getText());
            }
            dados.add(this.campoCRM.getText());
            dados.add(this.campoEmail.getText());
            dados.add(this.listaConvenios.getSelectedValuesList().toString());
            dados.add(this.campoEspecialidade.getText());
            dados.add(this.campoFormacoes.getText());
            
            geral.Utils.checaVazio(dados);

            DAOMedico daoMedico = new DAOMedico();
            String op;
            if(!cadastroExistente){
                op = "Salvo";
                daoMedico.insereMedico(new Medico(dados));
                this.menuAnterior.setVisible(true);
            }else{
                op = "Atualizado";
                daoMedico.alteraMedico(new Medico(dados));
                ((Menu) ((Tabela) this.menuAnterior).menuAnterior).abreTabela(1);
                this.menuAnterior.dispose();
            }
            JOptionPane.showMessageDialog(null, "Registro " + op);
            
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), 
                    "Erro", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed


    //Construtor para a busca de Medico
    public CadastroMedico(JFrame menuAnterior, Medico medico){
        this(menuAnterior);
        this.cadastroExistente = true;
        
        this.campoNome.setText(medico.getNome());
        this.campoTelefone.setText(medico.getTelefone());
        this.campoEndereco.setText(medico.getEndereco());
        
        if(medico.getNumIdentificador().matches("...\\....\\....-..")){
            this.opcaoCPF.doClick();
            this.campoCPF.setText(medico.getNumIdentificador());
        }else{
            this.opcaoCNPJ.doClick();
            this.campoCNPJ.setText(medico.getNumIdentificador());
        }
        this.campoCRM.setText(medico.getCrm());
        this.campoCRM.setEditable(false);
        this.campoEmail.setText(medico.getEmail());
        
        
        //Clica em todos os convenios que o medico possui na sua lista
        List lista = Arrays.asList((medico.getConvenios().replaceAll("[\\[\\]]", "")
                .replace(", ", ",").split(",")));
        int[] selecionados = new int[this.listaConvenios.getModel().getSize()];
        int k = 0;
        ListModel modeloLista = this.listaConvenios.getModel();
        for(Object o : lista){
            for(int i = 0; i < modeloLista.getSize(); i++){
                if(o.toString().equals(modeloLista.getElementAt(i))){
                    selecionados[k] = i;
                    k++;
                }
            }
        }
        this.listaConvenios.setSelectedIndices(selecionados);
        
        this.campoEspecialidade.setText(medico.getEspecialidade());
        this.campoFormacoes.setText(medico.getFormacoes());
    }


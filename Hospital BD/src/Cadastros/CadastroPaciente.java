    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        try {
            ArrayList<Object> dados = new ArrayList<>();
            dados.add(this.campoNome.getText());
            String dataNasc = this.campoNascimento.getText();
            geral.Utils.converteStringToDate(dataNasc);
            dados.add(this.campoNascimento.getText());
            dados.add(this.opcaoMasc.isSelected() ? 'M' : 'F');
            dados.add(this.campoSangue.getText());
            dados.add(this.campoEndereco.getText());
            String cpf = this.campoCPF.getText();
            geral.Utils.validaCpf(cpf);
            dados.add(cpf);
            dados.add(this.comboConvenio.getSelectedItem().toString());
            dados.add(this.campoTelefone.getText());
            dados.add(this.campoHistorico.getText());
            
            geral.Utils.checaVazio(dados);
            
            DAOPaciente daoPaciente = new DAOPaciente();
            this.dispose();
            String op;
            if(!cadastroExistente){
                op = "Salvo";
                daoPaciente.inserePaciente(new Paciente(dados));
                this.menuAnterior.setVisible(true);
            }else{
                op = "Atualizado";
                daoPaciente.alteraPaciente(new Paciente(dados));
                ((Menu) ((Tabela) this.menuAnterior).menuAnterior).abreTabela(0);
                this.menuAnterior.dispose();
            }
            JOptionPane.showMessageDialog(null, "Registro " + op);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), 
                    "Erro", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed


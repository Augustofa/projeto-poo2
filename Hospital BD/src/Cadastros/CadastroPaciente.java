    public CadastroPaciente(JFrame menuAnterior, Paciente paciente){
        this(menuAnterior);
        this.cadastroExistente = true;
        
        this.campoNome.setText(paciente.getNome());
        this.campoNascimento.setText(paciente.getDataNascimento());
        
        if(paciente.getSexo() == 'M'){
            this.opcaoMasc.doClick();
        }else{
            this.opcaoFem.doClick();
        }
            
        this.campoSangue.setText(paciente.getTipoSanguineo());
        this.campoEndereco.setText(paciente.getEndereco());
        this.campoCPF.setText(paciente.getCpf());
        this.campoCPF.setEditable(false);
        this.comboConvenio.setSelectedItem(paciente.getConvenio());
        this.campoTelefone.setText(paciente.getTelefone());
        this.campoHistorico.setText(paciente.getHistorico());
    }
    
